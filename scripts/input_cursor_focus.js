var ONLY_WHITE_LIST = true;  // 只允许白名单
var WHITE_LIST = [
    "baidu.com", "google.com", "taobao.com", "youtube.com",
];


(function () {
    setTimeout(function(){
        main();
    },10);
})();

function main() {
    var can_run = true;
    if (ONLY_WHITE_LIST) {
        can_run = false;
        for (var i = 0; i < WHITE_LIST.length; i++) {
            var site = WHITE_LIST[i];
            if (Mjztool.matchURL(site)) {
                can_run = true;
                break;
            }
        }
    }

    if (can_run) {
        inputFocus();
    }
}

function inputFocus(){
    if (document.activeElement.tagName != "INPUT") {
        var inputs = document.body.getElementsByTagName("input");
        focused = false;
        for (var i=0; i<inputs.length; i++) {
            var input = inputs[i];
            if (elementInViewport(input) && input.type=="text") {
                input.focus();
                focused = true;
                break;
            }
        }
        
        if (!focused && inputs.length>0)
          for (var i=0; i<inputs.length; i++) {
            var input = inputs[i];
            if (elementInViewport(input)) {
                input.focus();
                focused = true;
                break;
            }
        }
    }
}
  
function elementInViewport(el) {
    var top = el.offsetTop;
    var left = el.offsetLeft;
    var width = el.offsetWidth;
    var height = el.offsetHeight;

    while(el.offsetParent) {
        el = el.offsetParent;
        top += el.offsetTop;
        left += el.offsetLeft;
    }

    return (
        top >= window.pageYOffset &&
        left >= window.pageXOffset &&
        (top + height) <= (window.pageYOffset + window.innerHeight) &&
        (left + width) <= (window.pageXOffset + window.innerWidth)
    );
}

